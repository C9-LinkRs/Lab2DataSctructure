    private void crearcbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crearcbtActionPerformed
       Nodo aux = db.getptr();
        int cuenta=Integer.parseInt(numcuentatf.getText());
        if (CrearCbuscar(aux, cuenta) && Integer.parseInt(montotf.getText())>=100000) {
            db.add(fechas4.getDate(),Long.parseLong(numcuentatf.getText()),Long.parseLong(montotf.getText()),1);
            JOptionPane.showMessageDialog(null,"Cuenta creada exitosamente!");
            numcuentatf.setText(null);
            montotf.setText(null);
        }
    }//GEN-LAST:event_crearcbtActionPerformed

    public boolean CrearCbuscar(Nodo aux, int numero){
        if (aux!=null) {
            if (aux.NroCta == Integer.parseInt(numcuentatf.getText())) {
                JOptionPane.showMessageDialog(null,"Cuenta ya existente!");
                return false;
            }else return CrearCbuscar(aux.Nazi,numero);
        }
        return true;
    }
    
