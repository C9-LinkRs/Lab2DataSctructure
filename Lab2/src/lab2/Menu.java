    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        long monto = 0;
        if (tf5 != null) {
            Nodo aux = db.getptr();
            while (aux != null) {
                if (aux.NroCta == Integer.parseInt(tf4.getText())) {
                    if (aux.tipo == 1) {
                        monto = monto + aux.monto;
                    }
                    if (aux.tipo == 2) {
                        monto = monto - aux.monto;
                    }
                }
                aux=aux.Nazi;
            }
            if (monto == 0) {
                db.delete(Integer.parseInt(tf4.getText()));
                JOptionPane.showMessageDialog(null,"Cuenta eliminada exitosamente!");
            }else JOptionPane.showMessageDialog(null,"Opcion no disponible para esta cuenta!");
        }
    }//GEN-LAST:event_jButton7ActionPerformed

