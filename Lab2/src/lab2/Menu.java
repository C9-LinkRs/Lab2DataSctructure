    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
    long monto = 0;
        if (tf4 != null) {
            Nodo aux = db.getptr();
            while (aux != null) {
                if (aux.NroCta == Integer.parseInt(tf4.getText())) {
                    if (aux.tipo == 1) {
                        monto = monto + aux.monto;
                    }
                    if (aux.tipo == 2) {
                        monto = monto - aux.monto;
                    }
                }
                aux=aux.Nazi;
            }
        }
        JOptionPane.showMessageDialog(null, "El monto es de: " + monto);
    }//GEN-LAST:event_jButton6ActionPerformed

